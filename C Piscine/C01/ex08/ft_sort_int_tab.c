void	ft_sort_int_tab(int *tab, int size)
{
	int		i;
	int		temp;
	int		j;

	temp = 0;
	j = 0;
	while (j < size - 1)
	{
		i = 0;
		while (i < size - 1 - j)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
			}
			i++;
		}
		j++;
	}
}
